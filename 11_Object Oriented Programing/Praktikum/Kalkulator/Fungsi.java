package Kalkulator;

public class Fungsi {
    public static Integer penjumlahan(int a, int b){
        int jumlah = a + b ;
        return jumlah ;
    }
    public static Integer pengurangan(int a, int b){
        int kurang = a - b ;
        return kurang ;
    }
    public static Integer perkalian(int a, int b){
        int kali = a * b ;
        return kali ;
    }
    public static Integer pembagian(int a, int b){
        int bagi = a / b ;
        return bagi ;
    }
}

